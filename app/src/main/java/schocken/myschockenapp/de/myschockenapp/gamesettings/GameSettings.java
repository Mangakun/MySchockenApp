package schocken.myschockenapp.de.myschockenapp.gamesettings;

/**
 * An interface which contains game settings
 */
public interface GameSettings {

    /**
     * Max dice throws a player has
     */
    final int MAXDICETRHOWS = 3;

    /**
     * Max dice size a player has
     */
    final int MAXDICESIZE = 3;

    /**
     * Max coasters
     */
    final int MAXCOASTERS = 13;
}
